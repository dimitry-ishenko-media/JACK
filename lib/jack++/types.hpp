enum dir { in = 0x01, out = 0x02 };

constexpr dir operator|(dir x, dir y) { return static_cast<dir>(static_cast<int>(x) | static_cast<int>(y)); }
constexpr dir operator&(dir x, dir y) { return static_cast<dir>(static_cast<int>(x) & static_cast<int>(y)); }

